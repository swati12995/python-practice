'''
for loop is used to execute a block of code for every iteration
syntax of a for loop
my_iterable =[1,2,3]
for item_name in my_iterable:
    print(item_name)
'''
myli = [3, 4, 5, 6, 7, 8, 10]
for itn in myli:
    print(itn, "Hello")
for n in myli:
    if n % 2 == 0:
        print(n, " is an even number.")
    else:
        print(n, " is an odd number.")
